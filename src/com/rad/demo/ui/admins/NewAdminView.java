    public void addNewAdmin(String title, String name, String role, String nic, String phone, String mail, int maxId) {
        String last_logged = "0000-00-00 00:00:00";
        int admin_id = maxId + 1;
        String g = String.valueOf(admin_id);
        int l = g.length();
        String un = null;
        switch (l) {
            case 1:
                un = "000" + g;
                break;
            case 2:
                un = "00" + g;
                break;
            case 3:
                un = "0" + g;
                break;
            case 4:
                un = g;
                break;
            default:
                JOptionPane.showMessageDialog(this, "Sorry, Maximum number of admins is reached!", "Warning", JOptionPane.WARNING_MESSAGE);
                this.dispose();
                break;
        }
        String uname = "lab" + un;
        String password = role.toLowerCase();

        AdminsSQL.addNewAdmin(0, title, name, role, nic, phone, mail, uname, password, last_logged, 0, 1);
        JOptionPane.showMessageDialog(this, role + " was added successfully.\n\nUser Name : " + uname + "\nPassword : " + password + "\n", "", JOptionPane.INFORMATION_MESSAGE);
        this.dispose();
    }

