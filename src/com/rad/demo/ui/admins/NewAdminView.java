    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        int maxid = 0;
        try {
            maxid = AdminsSQL.getMaxId();
        } catch (SQLException ex) {
            Logger.getLogger(NewAdminView.class.getName()).log(Level.SEVERE, null, ex);
        }

        btnAdmin.setActionCommand("Admin");
        btnHead.setActionCommand("Head");

        String title1 = txtTitle.getText();
        String name = txtName.getText();
        String role = grpRole.getSelection().getActionCommand();
        String nic = txtNic.getText();
        String phone = txtPhone.getText();
        String mail = txtMail.getText();

        int id = 0;
        String input = null;
        try {
            id = AdminsSQL.getIdByName(name);
        } catch (SQLException ex) {
            Logger.getLogger(EditProfileView.class.getName()).log(Level.SEVERE, null, ex);
        }

        if (title1.equals("")) {
            JOptionPane.showMessageDialog(this, "Please enter a title!", "", JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        if (name.equals("")) {
            JOptionPane.showMessageDialog(this, "Please enter a name!", "", JOptionPane.INFORMATION_MESSAGE);
            return;
        }

        if (txtNic.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Enter NIC number");
            return;
        }
        Pattern nicp = Pattern.compile("(\\d{9}[V])*");
        Matcher m = nicp.matcher(txtNic.getText());
        if (!m.matches()) {
            JOptionPane.showMessageDialog(this, "Enter valid NIC number");
            return;
        }

        if (txtPhone.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Enter contact number");
            return;
        }
        Pattern pp = Pattern.compile("(\\d{10})*");
        m = pp.matcher(txtPhone.getText());
        if (!m.matches()) {
            JOptionPane.showMessageDialog(this, "Enter valid contact number");
            return;
        }

        if (txtMail.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Enter E-mail");
            return;
        }
        Pattern ep = Pattern.compile("(^[A-Z0-9._%+-]+@[A-Z0-9.-]+\\.[A-Z]{2,6}$)*", Pattern.CASE_INSENSITIVE);
        m = ep.matcher(txtMail.getText());
        if (!m.matches()) {
            JOptionPane.showMessageDialog(this, "Enter valid E-mail");
            return;
        }

        if (id == 0) {
            addNewAdmin(title1, name, role, nic, phone, mail, maxid);
        } else {
            JOptionPane.showMessageDialog(this, "This name already exists!", "Warning", JOptionPane.WARNING_MESSAGE);
            txtName.setText("");
        }
    }//GEN-LAST:event_btnAddActionPerformed

